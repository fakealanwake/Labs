#!/bin/bash

# Проверка на запрос справки
if [ "$1" = "-h" ] || [ "$1" = "--help" ]; then
    cat << EOF
Использование: $0 [ОПЦИИ] <суффикс> <выходной_файл> <размер_блока>

Скрипт для поиска неисполняемых файлов с заданным суффиксом, размер которых
кратен заданному размеру блока.

Аргументы:
  суффикс           Суффикс файлов для поиска (например, .txt, .log, .sh)
  выходной_файл     Имя файла для сохранения результатов
  размер_блока      Размер блока для проверки кратности (целое положительное число)

Опции:
  -h, --help        Показать эту справку и выйти

Описание:
  Скрипт ищет все неисполняемые файлы в текущей директории и поддиректориях,
  имя которых оканчивается на заданный суффикс. Для каждого найденного файла
  проверяется, кратен ли его размер (в байтах) указанному размеру блока.
  
  Если размер файла кратен размеру блока, имя файла и его размер
  записываются в выходной файл.

Примеры:
  $0 .txt result.txt 512         Найти все .txt файлы, размер которых кратен 512 байт
  $0 .log logs.txt 1024          Найти все .log файлы, размер которых кратен 1024 байт
  $0 .sh scripts.txt 4096        Найти все .sh файлы, размер которых кратен 4096 байт

Выходной формат:
  Для каждого подходящего файла в выходной файл записывается строка:
  <путь_к_файлу> <размер_в_байтах>
EOF
    exit 0
fi

# Проверка количества аргументов
if [ $# -ne 3 ]; then
    echo "Ошибка: неверное количество аргументов"
    echo "Использование: $0 <суффикс> <выходной_файл> <размер_блока>"
    echo "Для справки: $0 --help"
    exit 1
fi

SUFFIX="$1"
OUTPUT_FILE="$2"
BLOCK_SIZE="$3"

# Проверка, что размер блока - положительное число
if [ "$BLOCK_SIZE" -le 0 ] 2>/dev/null; then
    echo "Ошибка: размер блока должен быть положительным целым числом"
    echo "Для справки: $0 --help"
    exit 1
fi

# Очищаем выходной файл перед записью
> "$OUTPUT_FILE"

# Поиск и обработка файлов
find . -type f -name "*$SUFFIX" -not -executable | while read -r file; do
    # Получаем размер файла в байтах
    size=$(stat -f%z "$file" 2>/dev/null || stat -c%s "$file" 2>/dev/null)
    
    # Проверяем, кратен ли размер файла размеру блока
    if [ $((size % BLOCK_SIZE)) -eq 0 ]; then
        # Записываем имя файла и его размер в выходной файл
        echo "$file $size" >> "$OUTPUT_FILE"
    fi
done

# Подсчитываем количество найденных файлов
count=$(wc -l < "$OUTPUT_FILE")
echo "Готово. Найдено файлов: $count"